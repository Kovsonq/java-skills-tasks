package course.functional.chapterOption;

public class A {
    public A(int x, double y) {
        this.x = x;
        this.y = y;
    }

    int x;
    double y;

    @Override
    public String toString() {
        return "A{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
